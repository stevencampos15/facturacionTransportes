    public void llenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        TipoUsuarioDAO tuDao = new TipoUsuarioDAO();
        List<TipoUsuario> lista = (List<TipoUsuario>) tuDao.consultar();
        Object[] fila = new Object[3];
        for (int i = 0; i < lista.size(); i++) {
            fila[0] = lista.get(i).getIdTipoUsuario();
            fila[1] = lista.get(i).getNombreTU();
            fila[2] = lista.get(i).getDescripcionTU();
            modelo.addRow(fila);
        }

        tblLista.setModel(modelo);
    }

